package com.jtouzy.cv.model.classes;

import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;

import com.jtouzy.dao.annotations.DAOTable;
import com.jtouzy.dao.annotations.DAOTableField;
import com.jtouzy.dao.db.DBType;

@DAOTable(tableName = "sai")
public class Season {
	public static final String ID = "numsai";
	public static final String START_YEAR = "prdsai";
	public static final String END_YEAR = "prfsai";
	public static final String CURRENT = "encsai";
	public static final String REGISTRATION_OPEN = "iscsai"; 
	
	@DAOTableField(id = true, autoGenerated = true, name = Season.ID, type = DBType.INTEGER)
	private Integer identifier;
	@DAOTableField(name = Season.START_YEAR, type = DBType.VARCHAR)
	@NotNull(message = "L'année de commencement doit être renseignée")
	@Size(max = 4, message = "La taille de l'année de commencement doit être au maximum de {max}")
	private String startYear;
	@DAOTableField(name = Season.END_YEAR, type = DBType.VARCHAR)
	@NotNull(message = "L'année de fin doit être renseignée")
	@Size(max = 4, message = "La taille de l'année de fin doit être au maximum de {max}")
	private String endYear;
	@DAOTableField(name = Season.CURRENT, type = DBType.BOOLEAN)
	@NotNull(message = "La zone 'Saison courante' doit être renseignée")
	private Boolean current;
	@DAOTableField(name = Season.REGISTRATION_OPEN, type = DBType.BOOLEAN)
	private Boolean registrationOpen;

	public Integer getIdentifier() {
		return identifier;
	}

	public void setIdentifier(Integer identifier) {
		this.identifier = identifier;
	}

	public String getStartYear() {
		return startYear;
	}

	public void setStartYear(String startYear) {
		this.startYear = startYear;
	}

	public String getEndYear() {
		return endYear;
	}

	public void setEndYear(String endYear) {
		this.endYear = endYear;
	}
	
	public Boolean isCurrent() {
		return current;
	}

	public void setCurrent(Boolean current) {
		this.current = current;
	}
	
	public Boolean isRegistrationOpen() {
		return registrationOpen;
	}

	public void setRegistrationOpen(Boolean registrationOpen) {
		this.registrationOpen = registrationOpen;
	}

	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("Season [identifier=");
		builder.append(identifier);
		builder.append(", startYear=");
		builder.append(startYear);
		builder.append(", endYear=");
		builder.append(endYear);
		builder.append(", current=");
		builder.append(current);
		builder.append(", registrationOpen=");
		builder.append(registrationOpen);
		builder.append("]");
		return builder.toString();
	}
}
