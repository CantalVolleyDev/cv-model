package com.jtouzy.cv.model.errors;

public class UtilsToolException extends Exception {
	private static final long serialVersionUID = 1L;

	public UtilsToolException() {
		super();
	}
	public UtilsToolException(String message, Throwable cause) {
		super(message, cause);
	}
	public UtilsToolException(String message) {
		super(message);
	}
	public UtilsToolException(Throwable cause) {
		super(cause);
	}
}
